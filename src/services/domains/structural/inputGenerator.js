/**
 * Generate CalculiX input file based on system type
 * @param {string} systemType - Type of structural system
 * @param {Object} params - Structural parameters
 * @returns {string} - CalculiX .inp file content
 */
export function generateInputFile(systemType, params) {
  const generators = {
    'Cantilever Beam': generateCantileverBeamInput,
    'Simply Supported Beam': generateSimplySupportedBeamInput,
    'Fixed Beam': generateCantileverBeamInput, // Similar structure
    'Column Buckling': generateCantileverBeamInput, // Placeholder
    'Plate Bending': generateCantileverBeamInput, // Placeholder
    'Shell Analysis': generateCantileverBeamInput, // Placeholder
    'Truss Analysis': generateCantileverBeamInput, // Placeholder
    'Frame Analysis': generateCantileverBeamInput // Placeholder
  };
  
  const generator = generators[systemType];
  if (!generator) {
    // Generic template for unknown system types
    return `*HEADING
${systemType}
Generated by SimForge
*END STEP`;
  }
  
  return generator(params);
}

/**
 * Parse parameters from input file (reverse operation)
 * @param {string} inputFile - CalculiX .inp file content
 * @returns {Object} - Extracted parameters
 */
export function parseInputFileParameters(inputFile) {
  const params = {};
  
  // Parse Young's modulus
  const elasticMatch = inputFile.match(/\*ELASTIC\s+([\d.eE+-]+)\s*,\s*([\d.eE+-]+)/);
  if (elasticMatch) {
    params.youngs_modulus = parseFloat(elasticMatch[1]);
    params.poisson_ratio = parseFloat(elasticMatch[2]);
  }
  
  // Parse density
  const densityMatch = inputFile.match(/\*DENSITY\s+([\d.eE+-]+)/);
  if (densityMatch) {
    params.density = parseFloat(densityMatch[1]);
  }
  
  // Parse load
  const loadMatch = inputFile.match(/\*CLOAD\s+[\w]+\s+\d+\s+-([\d.eE+-]+)/);
  if (loadMatch) {
    params.load_magnitude = parseFloat(loadMatch[1]);
  }
  
  return params;
}
