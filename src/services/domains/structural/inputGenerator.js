/**
 * Generate CalculiX input file for Cantilever Beam
 * @param {Object} params - Structural parameters
 * @returns {string} - CalculiX .inp file content
 */
function generateCantileverBeamInput(params) {
  const { 
    length = 100, 
    width = 20, 
    height = 20, 
    wall_thickness = 2, 
    ribs = 0,
    load_magnitude = 100, 
    youngs_modulus = 200000, 
    poisson_ratio = 0.29,
    density = 7850 
  } = params;
  
  // Convert to meters for CalculiX
  const L = length / 1000;
  const W = width / 1000;
  const H = height / 1000;
  const WT = wall_thickness / 1000;
  
  return `*HEADING
Cantilever Beam Analysis
Generated by SimForge

*PREPRINT, HISTORY=MODEL, FREQUENCY=0

*NODE, NSET=Nall
1, 0.0, 0.0, 0.0
2, ${L}, 0.0, 0.0
3, ${L}, ${W}, 0.0
4, 0.0, ${W}, 0.0
5, 0.0, 0.0, ${H}
6, ${L}, 0.0, ${H}
7, ${L}, ${W}, ${H}
8, 0.0, ${W}, ${H}

*ELEMENT, TYPE=C3D8R, ELSET=Eall
1, 1, 2, 3, 4, 5, 6, 7, 8

*MATERIAL, NAME=Steel
*ELASTIC
${youngs_modulus}, ${poisson_ratio}
*DENSITY
${density}

*SOLID SECTION, MATERIAL=Steel, ELSET=Eall

*BOUNDARY
Nfix, 1, 3

*CLOAD
Nload, 3, -${load_magnitude}

*STEP
*STATIC
*OUTPUT, FIELD
*NODE FILE
*EL FILE
*END STEP`;
}

