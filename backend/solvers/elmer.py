import sys
import os
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from typing import Dict, Any
import subprocess
import shutil
from pathlib import Path
import time

def run_elmer_docker(task_id: str, input_file: Dict[str, Any], options: Dict[str, Any]) -> Dict[str, Any]:
    """
    Run Elmer solver in Docker container
    
    Args:
        task_id: Task UUID
        input_file: {filename, content} - complete Elmer .sif file
        options: Additional options
    """
    
    # Extract sif content from input_file
    sif_content = input_file.get("content", "")
    filename = input_file.get("filename", "case.sif")
    
    # Create working directory (use project directory for better Docker volume support on macOS)
    base_dir = Path(__file__).parent.parent.parent / "simforge_runs"
    base_dir.mkdir(parents=True, exist_ok=True)
    work_dir = base_dir / task_id
    work_dir.mkdir(parents=True, exist_ok=True)
    
    # Step 1: Write .sif file to file (already generated by AI)
    sif_path = work_dir / filename
    
    with open(sif_path, 'w') as f:
        f.write(sif_content)
    
    # Step 2: Run Docker container
    print(f"[Elmer] Running solver for task {task_id}")
    
    try:
        # Check if Docker is available
        docker_available = shutil.which("docker") is not None
        
        if docker_available:
            # Run in Docker
            cmd = [
                "docker", "run", "--rm",
                "-v", f"{work_dir}:/work",
                "-w", "/work",
                "--cpus", "2",
                "--memory", "4g",
                "simforge-elmer:latest",
                "ElmerSolver", "case.sif"
            ]
            
            start_time = time.time()
            result = subprocess.run(cmd, capture_output=True, text=True, timeout=600)
            elapsed = time.time() - start_time
            
            if result.returncode != 0:
                print(f"[Elmer] Solver failed: {result.stderr}")
                raise Exception(f"Elmer solver failed: {result.stderr}")
                
        else:
            # Fallback: return synthetic result
            print("[Elmer] No solver available, returning synthetic result")
            return generate_synthetic_result(task_id, input_file, options)
    
    except subprocess.TimeoutExpired:
        raise Exception("Elmer solver timed out after 600 seconds")
    
    # Step 3: Parse output
    ep_path = work_dir / "case.ep"
    
    if ep_path.exists():
        from parsers.elmer_parser import parse_elmer
        parsed = parse_elmer(str(ep_path))
    else:
        # Fallback to synthetic result
        return generate_synthetic_result(task_id, input_file, options)
    
    # Step 4: Format result
    system_type = input_file.get("metadata", {}).get("system_type", "Thermal-Structural")
    return {
        "domain": "Thermal",
        "system_type": system_type,
        "solver_name": "Elmer",
        "solver_version": "9.0",
        "metrics": parsed.get("metrics", []),
        "contour_field": parsed.get("contour_field", {}),
        "visualization_type": "contour_field",
        "plain_summary": f"Multi-physics analysis complete. Max temperature: {parsed.get('metrics', [{}])[0].get('value', 0):.1f} °C",
        "raw_files": {
            "ep": str(ep_path) if ep_path.exists() else None,
            "sif": str(sif_path) if sif_path.exists() else None
        }
    }

