def generate_synthetic_result(task_id: str, input_file: Dict[str, Any], options: Dict[str, Any]) -> Dict[str, Any]:
    """Generate synthetic multi-physics result (fallback when solver unavailable)
    
    Note: Uses placeholder defaults since model parameters are not available in this fallback path.
    """
    # Placeholder defaults for synthetic result
    power = 25.0  # W, default fallback
    width = 50.0  # mm, default fallback
    height = 50.0  # mm, default fallback
    thermal_cond = 200.0  # W/(m·K), aluminum default
    youngs_mod = 69.0  # GPa, aluminum default
    thermal_exp = 23e-6  # 1/K, aluminum default
    
    # Simple thermal conduction
    area = (width * height) / 1e6  # m²
    max_temp = 293.15 + power / (thermal_cond * area) * 0.01  # Approximate
    
    # Thermal stress
    delta_T = max_temp - 293.15
    thermal_stress = youngs_mod * 1e9 * thermal_exp * delta_T / 1e6  # MPa
    
    # Generate synthetic contour field
    n_points = 100
    x = [i * width / (n_points - 1) for i in range(n_points)]
    y = [0] * n_points
    z = [0] * n_points
    temperature = [max_temp - (max_temp - 293.15) * (xi / width) for xi in x]
    stress = [thermal_stress * (1 - xi / width) for xi in x]
    
    return {
        "domain": "Thermal",
        "system_type": "Thermal-Structural",
        "solver_name": "Elmer",
        "solver_version": "9.0",
        "metrics": [
            {"name": "Max Temperature", "value": f"{max_temp:.1f}", "unit": "K"},
            {"name": "Thermal Stress", "value": f"{thermal_stress:.1f}", "unit": "MPa"},
            {"name": "Note", "value": "Synthetic result - solver unavailable", "unit": ""}
        ],
        "contour_field": {
            "x": x,
            "y": y,
            "z": z,
            "temperature": temperature,
            "stress": stress
        },
        "visualization_type": "contour_field",
        "plain_summary": f"Synthetic multi-physics result (solver unavailable). Max temp: {max_temp:.1f} K"
    }
